# operadores matemáticos

'''
- Soma: +
- Subtração: -
- Divisão: /
- Multiplicação: *
- Divisão inteira: //
- Resto da divisão: % mod
- Potência: **
'''

print("Soma: ", 10 + 20)
print("Subtração: ", 10 - 20)
print("Multiplicação: ", 10 * 20)
print("Potência: ", 10 ** 20)
print("Divisão: ", 10 / 20)
print("Divisão inteira: ", 10 // 20)
print("Resto de divisão: ",  10 % 20)

# Operadores booleanos
idade1 = 15
idade2 = 20
altura1 = 1.77
altura2 = 1.55
altura3 = altura2

print(idade1 > idade2)
print(idade1 <= idade2)
print('Maria' == 'maria') # diferente, pois o python compara textos se letras estão minusculas e maiusculas
print('arroz' != 'feijão')
print(altura1 >= altura2)
print(altura2 > altura3)

